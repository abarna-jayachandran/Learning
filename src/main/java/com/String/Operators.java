package com.String;

import java.util.ArrayList;

public class Operators {
    public static void main(String[] args) {
        System.out.println('a'+'b');//adding the ASCII values
        System.out.println("a"+"b");// concatenate and print as ab
        System.out.println('a'+(3));// Adding a ASCII values + 3= 100
        System.out.println((char)('a'+3)); //adding 3 character with char a so d
        System.out.println("a"+1);//concatenate and print as a1
        System.out.println("Abarna"+new ArrayList<>());//Print atring +empty array



    }
}
